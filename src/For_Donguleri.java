public class For_Donguleri {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("-------------------");

        int k = 1;
        while (k <=10){
            System.out.println(k);
            k +=2;
        }
    }
}
